package chapter_8;

public class Friends 
{
	private String name;
	private int number;
	
	public Friends() 
	{
		this.name = name;
		this.number = number;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String display() 
	{
		return "Name: " +name;
	
	}

	
	
}

	
	

